using UnityEngine;
using UnityEngine.UI;

public class ScoreScript : MonoBehaviour {

    //-------------------------------------------------------------------------
    // Varibles
    public int      score;
    private Text     scoreText;
    //-------------------------------------------------------------------------

    //-------------------------------------------------------------------------
    private void Awake ()
    {
        scoreText = GetComponent<Text> ();
    }
    //-------------------------------------------------------------------------

    //-------------------------------------------------------------------------
    private void Start () {
        SetScoreText ();
    }
    //-------------------------------------------------------------------------

    //-------------------------------------------------------------------------
    private void Update () {
        SetScoreText ();
	}
    //-------------------------------------------------------------------------

    //-------------------------------------------------------------------------
    private void SetScoreText ()
    {
        scoreText.text = "Score: " + score.ToString ();
    }
    //-------------------------------------------------------------------------

    //-------------------------------------------------------------------------
    public void IncrementScore ()
    {
        score += 1;
    }
    //-------------------------------------------------------------------------

}
