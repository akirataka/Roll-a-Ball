using UnityEngine;
using UnityEngine.UI;

public class WinScore : MonoBehaviour {

    //-------------------------------------------------------------------------
    // Varibles
    private Text     winText;

    public ScoreScript scoreScript;
    //-------------------------------------------------------------------------

    //-------------------------------------------------------------------------
    private void Awake ()
    {
        winText = GetComponent<Text> ();
    }
    //-------------------------------------------------------------------------

    //-------------------------------------------------------------------------
    private void Start ()
    {
        winText.text = "";
    }
    //-------------------------------------------------------------------------

    //-------------------------------------------------------------------------
    private void Update ()
    {
        if (scoreScript.score >= 12 ) {
            winText.text = "You Win!";
        }
    }
    //-------------------------------------------------------------------------

}
